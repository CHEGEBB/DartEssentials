// Using switch-case statements for discrete values

void main() {
  String day = 'Tuesday';

  switch (day) {
    case 'Monday':
      print('Start of the week');
      break;
    case 'Tuesday':
      print('Second day');
      break;
    case 'Friday':
      print('Weekend is near!');
      break;
    default:
      print('Just another day');
  }
}
