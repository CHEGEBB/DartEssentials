// This file explains the use of 'final' and 'const' in Dart

// final: Assigned once during runtime.

// const: Assigned at compile time (must be known beforehand).

// Both are used to create constants.
void main() {
  final String country = 'Kenya';
  const double pi = 3.14159;

  print('Country: $country');
  print('Pi: $pi');
}
